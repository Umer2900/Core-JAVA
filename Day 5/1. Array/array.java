/*  
    **Array**
    Array is a Linear data structure which has a collection of similar data types and each data item of an Array 
    is called an element. The elements of an Array are stored in contiguous memory locations.

    The purpose of Array is to use same variable again & again many times, 
    as it is capable of storing multiple values.
*/

class Demo
{
    public static void main(String[] args)
    {
        int arr[] = {1, 4, 6, 2};

        System.out.println(arr[1]);       // 4
        System.out.println(arr[0]);       // 1
        System.out.println(arr[2]);       // 6
        System.out.println(arr[3]);       // 2
    }
}